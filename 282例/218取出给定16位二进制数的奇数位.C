#include<stdio.h>

void main()
{
    unsigned short a,s=0,q;
    int i,j,n=7,m;
	printf("请输入一个八进制数:\n");
    scanf("%o", &a);										/*输入一个八进制数*/
    m=1<<15;											/*m的最高位为1，其他位为0*/
	a<<=1;											/*左移一位，使第15位成为最高位*/
	for(i=1;i<=8;i++)									/*得到8位数*/
	{	
		q=1;
		if(m & a)										/*如果本位上值为1则进行计算*/
		{	
			for(j=1;j<=n;j++)
				q*=2;								/*得到权值*/
			s+=q;									/*累加*/
		}
		a<<=2;										/*向左移位*/
		n--;	
	}
    printf("结果是:%o\n", s);							/*将结果输出*/
}
