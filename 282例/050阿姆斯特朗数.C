#include <stdio.h>

void main()
{
    int i, j, k, n;												/*定义变量为基本整型*/
    for (i = 100; i < 1000; i++)								/*对100~1000内的数进行穷举*/
    {
        j = i % 10; 											/*分离出个位上的数*/
        k = i / 10 % 10; 										/*分离出十位上的数*/
        n = i / 100; 											/*分离出百位上的数*/
        if (j *j * j + k * k * k + n * n * n == i) 				/*判断各位上的立方和是否等于其本身*/
            printf("%5d", i); 									/*将水仙花数输出*/
    }
	printf("\n");
}
