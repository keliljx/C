#include <stdio.h>

void main()
{
    int i;											/*定义整型变量i*/
    long f[31];										/*意义数组为长整形*/
    f[1] = 1, f[2] = 1;									/*数组中的f[1]、f[2]赋初值为1*/
    for (i = 3; i < 31; i++)
        f[i] = f[i - 1] + f[i - 2];							/*数列中从第3项开始每一项等于前两项之和*/
    for (i = 1; i < 31; i++)
    {
        printf("%10ld", f[i]);							/*输出数组中的30个元素*/
        if (i % 5 == 0)
            printf("\n");								/*每5个元素进行一次换行*/
    }
}
