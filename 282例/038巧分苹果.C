#include <stdio.h>

void main()
{
    int x[7], y[7], s, i;
    s = 2520 / 6; 										/*求出平均每个人要分多少个苹果*/
    for (i = 2; i <= 6; i++)
     /*求从老二到老六得到哥哥分来的苹果却未分给弟弟时的苹果数*/
        y[i] = s *(9-i) / (8-i);
    y[1] = x[1] = (s - y[6] / 3) *8 / 7;
     /*老大得到老六分来的苹果却未分给弟弟时的苹果数*/
    for (i = 2; i <= 6; i++)
        x[i] = y[i] - y[i - 1] / (10-i);				/*求原来每人得到的苹果数*/
    for (i = 1; i <= 6; i++)
        printf("x[%d]=%d\n", i, x[i]);					/*将最终结果输出*/
}
