#include<stdio.h>

void main()
{
    int i, a, b;											/*定义变量为基本整型*/
    for (i = 1000; i < 10000; i++)								/*对1000~10000间数进行穷举*/
    {
        a = i / 100; 										/*求出该数的前两位数*/
        b = i % 100; 										/*求出该数的后两位数*/
        if ((a + b)*(a + b) == i) 								/*判断是否满足条件*/
            printf("\n%5d", i);
    }
	printf("\n");
}

